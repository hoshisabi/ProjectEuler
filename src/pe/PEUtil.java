  public static long getBottomDigit(long num)
  {
    return num % 10;
  }

  public static int howManyDigits(long num)
  {
    int d = 0;

    for (long tt = num; tt > 0; tt /= 10)
    {
      d++;
    }

    return d;
  }

  public static long[] getLongAsDigits(long num)
  {
    int numDigits = howManyDigits(num);
    long retArr[] = new long[numDigits];
    long accum = num;

    for (int i = 0; i < numDigits; i++)
    {
      retArr[i] = accum % 10;
      accum /= 10;
    }

    reverse(retArr);
    return retArr;
  }

  private static long[] reverse(long[] arr)
  {
    long reverseArr[] = new long[arr.length];
    for (int i = 0; i < arr.length; i++)
    {
      reverseArr[arr.length - i] = arr[i];
    }
    return reverseArr;
  }

  public static long[] rotateArr(long[] digits, int howMany)
  {
    int length = digits.length;
    long[] rotatedArr = new long[length];
    for (int i = 0; i < length; i++)
    {
      int sourceIndex = i - howMany;
      if (sourceIndex < 0)
      {
        sourceIndex += length;
      }
      rotatedArr[i] = digits[sourceIndex];
    }
    return rotatedArr;
  }

  public static long convertDigitsToLong(long[] digits)
  {
    long accum = 0;
    for (long digit : digits)
    {
      accum *= 10;
      accum += digit;
    }
    return accum;
  }

  public static boolean isCircular(long num)
  {
    long[] asDigits = getLongAsDigits(num);
    int length = asDigits.length;
    if (length < 2)
    {
      return true;
    }

    for (int i = 0; i < length; i++)
    {
      long[] rotatedArr = rotateArr(asDigits, i);
      long rotatedNum = convertDigitsToLong(rotatedArr);
      boolean isPrime = contains(primesArr, rotatedNum);
      if (!isPrime)
      {
        return false;
      }
    }

    return true;
  }

  private static boolean contains(long[] primesArr, long rotatedNum)
  {
    for (Long aLong : primesArr)
    {
      if (aLong == rotatedNum)
      {
        return true;
      }
    }
    return false;
  }

  public static List<Integer> listAllBut(List<Integer> allowedDigits, Integer digit)
  {
    int pos = allowedDigits.indexOf(digit);
    List<Integer> sublist = new ArrayList<Integer>(allowedDigits);
    sublist.remove(pos);
    return sublist;
  }


